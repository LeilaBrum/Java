package Arrays;

public class ArrayBiDimensional {
	public static void main (String[] args) {
		String [] [] duasdim = {{"Carlos", "M", "SP"}, {"Lidiana", "F", "SP"}};
		
		/*Mostrando as informações do 1º array*/
		System.out.println(duasdim[0] [0]);
		System.out.println(duasdim[0] [1]);
		System.out.println(duasdim[0] [2]);
		
		/*Mostrando as informações do 2º array*/
		System.out.println(duasdim[1] [0]);
		System.out.println(duasdim[1] [1]);
		System.out.println(duasdim[1] [2]);
	}
}
